    const waveOne = ".wave-one";
    const waveTwo = ".wave-two";
    const waveThree = ".wave-three";
    const waveFour = ".wave-four";


  gsap.to(waveOne, {
    y: 20,
    duration: 1.5,
    repeat: -1,
    yoyo: true,
    ease: 'power1.inOut'
  });

  gsap.to(waveTwo, {
    y: -10,
    duration: 1.2,
    repeat: -1,
    yoyo: true,
    ease: 'power1.inOut',
    delay: 0.3
  });

  gsap.to(waveThree, {
    y: -30,
    duration: 1.8,
    repeat: -1,
    yoyo: true,
    ease: 'power1.inOut',
    delay: 0.6
  });

  gsap.to(waveFour, {
    y: -30,
    duration: 2,
    repeat: -1,
    yoyo: true,
    ease: 'power1.inOut',
    delay: 0.9
  });
};

// Appel de la mÃ©thode au montage du composant
onMounted(() => {
  swayBoat();
    animateWaves();
});


</script>

