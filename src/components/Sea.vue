<script setup lang="ts">
import { onMounted } from 'vue';
import { gsap } from 'gsap';

// ...

const swayBoat = () => {
  gsap.to(".boat", {
    rotation: "-5deg",
    duration: 5,
    yoyo: true,
    repeat: -1,
    ease: 'power1.inOut'
  });
};

